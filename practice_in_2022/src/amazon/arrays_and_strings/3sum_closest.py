class Solution:
    def threeSumClosest(self, nums: list[int], target: int) -> int:
        nums.sort()
        diff = float("inf")

        for i in range(len(nums)):
            low, high = i + 1, len(nums) - 1

            while low < high:
                new_sum = nums[i] + nums[low] + nums[high]

                if abs(target - new_sum) < abs(diff):
                    diff = target - new_sum

                if diff == 0:
                    break

                if new_sum <= target:
                    low += 1
                else:
                    high -= 1
        return target - diff


